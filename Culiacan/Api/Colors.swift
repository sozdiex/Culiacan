import UIKit
import Foundation

class Colors: NSObject {
    class func grisClaro() -> UIColor{
        return UIColor(red: 184/255, green: 184/255, blue: 185/255, alpha: 1.0)
    }
    
    class func negro() -> UIColor{
        return UIColor(red: 34/255, green: 31/255, blue: 32/255, alpha: 1.0)
    }
    
    class func tinto() -> UIColor{
        return UIColor(red: 142/255, green: 33/255, blue: 49/255, alpha: 1.0)
    }
    
    class func grisFuerte() -> UIColor{
        return UIColor(red: 66/255, green: 64/255, blue: 65/255, alpha: 1.0)
    }
}
